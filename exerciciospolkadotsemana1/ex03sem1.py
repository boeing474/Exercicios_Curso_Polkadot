n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))

soma = n1 + n2
sub = n1 - n2
divisao = n1/n2
multiplicacao = n1 * n2

print(f'A soma dos números {n1} e {n2} = {soma}')
print(f'A subtração dos números {n1} e {n2} = {sub}')
print(f'A divisão dos números {n1} e {n2} = {divisao}')
print(f'A multiplicação dos números {n1} e {n2} = {multiplicacao}')
